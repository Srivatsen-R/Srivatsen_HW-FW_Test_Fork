//main function. 
int main(void) {

  //system clock init.
  SYSTEM_INIT();

  //function to keep drive disable intially.
  MotorControl_Init();

  //can init
  can.setup();

  //adc init and start via DMA
  if(analog.start_dma(&hadc1, (uint32_t*) analog.bufferData, ADC_NoOfConversion) != OK) {
     Error_Handler();
  }

  HAL_Delay(5000);

  //enabling motor control interrupts , ABZ+PWM sensing interrpts.
  ENABLE_PERIPHERALS();

  Pegasus_MBD_initialize();
  
  //while loop running on CLK frequency.
  while (1) 
  {
    uint32_t time_count = HAL_GetTick();
    static uint32_t prev_time = 0;
    static uint32_t prev_thr_time = 0;

    ANALOG_READING();

    if (time_count - prev_time >= 11.0)
    {
      send_on_300();
      send_on_301();
      send_on_302();

      prev_time = time_count;
    }

    if (time_count - prev_thr_time >= 100)
    {
      rtU.Torque = map(analog.bufferData[THROTTLE], 8900.0, 40000.0, 0.0, 80.0);

      prev_thr_time = time_count;
    }
  }
}

