void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)  {
  /* Prevent unused argument(s) compilation warning */
  
  if(htim->Instance == TIM7)
  {
    motorControl.drive.check = DRIVE_DISABLE;
    HAL_TIM_Base_Stop_IT(&htim17);
    switch (upgrade_state)
    {
    case UPGRADE_INIT:
      handle_upgrade_init();
      break;
    case UPGRADE_RECEIVE_DATA:
      handle_rceive_data();
      break;
    case UPGRADE_PAUSE:
      handle_upgrade_pause();
      HAL_TIM_Base_Stop_IT(&htim7);
      motorControl.drive.check = DRIVE_ENABLE;
      HAL_TIM_Base_Start_IT(&htim17);
      break;
    case UPGRADE_RESUME:
      handle_upgrade_resume();
      break;
    case UPGRADE_COMPLETE:
      handle_upgrade_complete();
      break;
    case UPGRADE_FAILED:
      HAL_TIM_Base_Stop_IT(&htim7);
      motorControl.drive.check = DRIVE_ENABLE;
      HAL_TIM_Base_Start_IT(&htim17);
      break;
    default:
      break;
    }
  }

  if(htim->Instance==TIM17) {
  //turn led red/green depending on drive status
    if(motorControl.drive.check == DRIVE_DISABLE) {
      //rgb gpio
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_SET);
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_11, GPIO_PIN_SET);
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);

      DRIVE_STOP();
    }
    else if(motorControl.drive.check == DRIVE_ENABLE) {
      //  rgb gpio
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_10, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_11, GPIO_PIN_SET);
      HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
    }

    //motor angle,current
    READ_MOTOR_POSITION();

    FOC_READ_MOTOR_POSITION();

    READ_MOTOR_PHASE_CURRENT();

    FOC_step();

    FOC_SPACE_VECTOR_MODULATION();
  }
}


