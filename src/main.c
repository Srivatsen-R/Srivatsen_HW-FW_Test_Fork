//main function. 
int main(void) 
{
  //system clock init.
  SYSTEM_INIT();

  Update_BOR_Counter();

  Boot_Counter();

  read_hard_fault_cause();

  //function to keep drive disable intially.
  MotorControl_Init();

  //can init
  can.setup();

  //adc init and start via DMA
  if(analog.start_dma(&hadc1, (uint32_t*) analog.bufferData, ADC_NoOfConversion) != OK) 
  {
    Error_Handler();
  }

  HAL_Delay(2000);

  bootup_config();

  //enabling motor control interrupts , ABZ+PWM sensing interrpts.
  ENABLE_PERIPHERALS();

  Get_and_Send_UIID();

  Current_Sensor_offset_cal();

  FOC_initialize();
  
  //while loop running on CLK frequency.
  while (1) 
  {
    PVD_Counter_Update();

    CAN_Transmit_routine();

    ANALOG_READING();
    
    FAULT_DETECTION();

    Throttle_Control_routine();

    Led_Toggle();
  }
}

